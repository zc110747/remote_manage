/**
 * LED开关转换函数
 * 
 * @param nBeepStatus 设置LED的开关状态
 *  
 * @return NULL
 */
void LedStatusConvert(uint8_t nLedStatus)
{
    int nFd;
    uint8_t nVal;

    DRIVER_DEBUG("led write:%d\n", nLedStatus);
    nFd = open(LED_DEVICE, O_RDWR | O_NDELAY);
    if(nFd != -1)
    {
        nVal = nLedStatus;
        write(nFd, &nVal, 1);  //将数据写入LED
        close(nFd);
    }
}
